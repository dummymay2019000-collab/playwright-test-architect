import type { GeneratedTestCase, RequestConfig } from "./types";

function jsonLiteral(v: unknown): string {
  return JSON.stringify(v, null, 2);
}

function buildHeadersObject(config: RequestConfig): Record<string, string> {
  const out: Record<string, string> = { "Content-Type": "application/json" };
  for (const h of config.headers) {
    if (h.enabled && h.key.trim()) out[h.key.trim()] = h.value;
  }
  return out;
}

export type AttachmentMode = "separate" | "combined";

export interface SpecBuildOptions {
  attachmentMode?: AttachmentMode;
}

export function buildSpecFile(
  config: RequestConfig,
  cases: GeneratedTestCase[],
  options: SpecBuildOptions = {},
): string {
  const attachmentMode: AttachmentMode = options.attachmentMode ?? "separate";
  const enabled = cases.filter(c => c.enabled);
  const headers = buildHeadersObject(config);
  const tokenEnv = config.auth.envVarName || "API_TOKEN";
  const useEnvToken = config.auth.mode === "envBearer" || config.auth.mode === "staticBearer";

  let basePayload: unknown = {};
  try {
    basePayload = JSON.parse(config.bodyJson || "{}");
  } catch {
    basePayload = {};
  }

  const sanitizedCases = enabled.map(c => ({
    id: c.id,
    name: c.name,
    category: c.category,
    fieldPath: c.fieldPath ?? null,
    override: c.override ?? null,
    removePaths: c.removePaths ?? [],
    headersOverride: c.headersOverride ?? null,
    expectedStatus: c.expectedStatus,
    risk: c.risk,
    reason: c.reason,
  }));

  return `/**
 * ${config.apiName || "Generated API"} — Playwright API tests
 * Auto-generated by API TestForge.
 *
 * Drop this file into any Playwright TypeScript project:
 *   tests/${(config.apiName || "api").toLowerCase().replace(/\\s+/g, "-")}.spec.ts
 *
 * Required env vars:
 *   BASE_URL=${config.baseUrl}
${useEnvToken ? `*   ${tokenEnv}=your_token_here\n` : ""} */
import { test, expect, request as pwRequest } from "@playwright/test";
import * as fs from "fs";
import * as path from "path";
import "dotenv/config";

// ---------- Config ----------
const BASE_URL = process.env.BASE_URL || ${JSON.stringify(config.baseUrl)};
const ENDPOINT = ${JSON.stringify(config.endpoint)};
const METHOD = ${JSON.stringify(config.method)} as const;
const SUCCESS_STATUS = ${config.expectedSuccessStatus};
const VALIDATION_STATUS = ${config.expectedValidationStatus};

const BASE_HEADERS: Record<string, string> = ${jsonLiteral(headers)};

const BASE_PAYLOAD: Record<string, unknown> = ${jsonLiteral(basePayload)};

interface TestCase {
  id: string;
  name: string;
  category: string;
  fieldPath: string | null;
  override: Record<string, unknown> | null;
  removePaths: string[];
  headersOverride: Record<string, string | null> | null;
  expectedStatus: number;
  risk: "low" | "medium" | "high";
  reason: string;
}

const CASES: TestCase[] = ${jsonLiteral(sanitizedCases)};

// ---------- Helpers ----------
function deepClone<T>(v: T): T {
  return JSON.parse(JSON.stringify(v));
}

function setByPath(obj: any, dotPath: string, value: unknown): void {
  const parts = dotPath.split(".");
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    if (cur[parts[i]] == null || typeof cur[parts[i]] !== "object") cur[parts[i]] = {};
    cur = cur[parts[i]];
  }
  cur[parts[parts.length - 1]] = value;
}

function deleteByPath(obj: any, dotPath: string): void {
  const parts = dotPath.split(".");
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    if (cur[parts[i]] == null) return;
    cur = cur[parts[i]];
  }
  delete cur[parts[parts.length - 1]];
}

function buildPayload(tc: TestCase): unknown {
  const payload = deepClone(BASE_PAYLOAD);
  for (const p of tc.removePaths) deleteByPath(payload, p);
  if (tc.override) {
    for (const [k, v] of Object.entries(tc.override)) {
      if (k.includes(".")) setByPath(payload, k, v);
      else (payload as any)[k] = v;
    }
  }
  return payload;
}

function applyHeadersOverride(tc: TestCase): Record<string, string> {
  const headers = { ...BASE_HEADERS };
${useEnvToken ? `  const token = process.env.${tokenEnv};\n  if (token) headers["Authorization"] = \`Bearer \${token}\`;\n` : ""}  if (tc.headersOverride) {
    for (const [k, v] of Object.entries(tc.headersOverride)) {
      if (v === null) delete headers[k];
      else headers[k] = v;
    }
  }
  return headers;
}

function classifyResponse(status: number, expected: number): "pass" | "fail" {
  return status === expected ? "pass" : "fail";
}

interface CaseResult {
  id: string;
  name: string;
  category: string;
  expectedStatus: number;
  actualStatus: number;
  result: "pass" | "fail";
  risk: string;
  reason: string;
  request: {
    method: string;
    url: string;
    headers: Record<string, string>;
    body: unknown;
  };
  response: {
    status: number;
    headers: Record<string, string>;
    body: unknown;
  };
}
const results: CaseResult[] = [];

function safeParse(text: string): unknown {
  try { return JSON.parse(text); } catch { return text; }
}

function redactHeaders(h: Record<string, string>): Record<string, string> {
  const out: Record<string, string> = {};
  for (const [k, v] of Object.entries(h)) {
    if (/^authorization$|api[-_]?key|token|secret|cookie/i.test(k)) {
      out[k] = v ? "***redacted***" : v;
    } else {
      out[k] = v;
    }
  }
  return out;
}

async function attachJson(testInfo: unknown, name: string, data: unknown): Promise<void> {
  const info = testInfo as { attach?: (name: string, options: { body: string; contentType: string }) => Promise<void> | void } | undefined;

  if (typeof info?.attach === "function") {
    await info.attach(name, {
      body: JSON.stringify(data, null, 2),
      contentType: "application/json",
    });
  }
}

// ---------- Tests ----------
//
// HOW TO RUN A SINGLE TEST CASE:
//   Each test is tagged with @id-<caseId> and @<category>. Examples:
//     npx playwright test ${(config.apiName || "api").toLowerCase().replace(/\\s+/g, "-")}.spec.ts --grep "@id-<caseId>"
//     npx playwright test --grep "@positive"
//     npx playwright test -g "exact test name substring"
//   You can also use Playwright UI mode and click a single test:
//     npx playwright test --ui
//
// ATTACHMENT MODE: ${JSON.stringify(attachmentMode)}
//   - "separate": request.json and response.json are attached as two entries
//   - "combined": a single payloads.json contains { request, response }
const ATTACHMENT_MODE: "separate" | "combined" = ${JSON.stringify(attachmentMode)};

test.describe(${JSON.stringify(config.apiName || "API tests")}, () => {
  for (const tc of CASES) {
    const title = \`[\${tc.category}] \${tc.name} @id-\${tc.id} @\${tc.category}\`;
    test(title, async ({}, testInfo) => {
      const ctx = await pwRequest.newContext({ baseURL: BASE_URL });
      const headers = applyHeadersOverride(tc);
      const payload = buildPayload(tc);
      const requestBody = METHOD === "GET" ? undefined : payload;

      const requestSnapshot = {
        method: METHOD,
        url: \`\${BASE_URL}\${ENDPOINT}\`,
        headers: redactHeaders(headers),
        body: requestBody ?? null,
      };

      // Attach request payload only in "separate" mode (combined attaches once after response)
      if (ATTACHMENT_MODE === "separate") {
        await attachJson(testInfo, "request.json", requestSnapshot);
      }

      const response = await ctx.fetch(ENDPOINT, {
        method: METHOD,
        headers,
        data: requestBody,
      });

      const actual = response.status();
      const respText = await response.text();
      const respBody = safeParse(respText);
      const respHeaders = response.headers();

      const responseSnapshot = {
        status: actual,
        headers: respHeaders,
        body: respBody,
      };

      if (ATTACHMENT_MODE === "separate") {
        await attachJson(testInfo, "response.json", responseSnapshot);
      } else {
        // Combined: one tidy attachment with both payloads
        await attachJson(testInfo, "payloads.json", {
          request: requestSnapshot,
          response: responseSnapshot,
        });
      }

      const result = classifyResponse(actual, tc.expectedStatus);
      results.push({
        id: tc.id,
        name: tc.name,
        category: tc.category,
        expectedStatus: tc.expectedStatus,
        actualStatus: actual,
        result,
        risk: tc.risk,
        reason: tc.reason,
        request: requestSnapshot,
        response: responseSnapshot,
      });

      expect.soft(actual, \`Expected \${tc.expectedStatus} for "\${tc.name}", got \${actual}\`).toBe(tc.expectedStatus);

      await ctx.dispose();
    });
  }

  test.afterAll(async () => {
    const passed = results.filter(r => r.result === "pass").length;
    const failed = results.length - passed;
    // eslint-disable-next-line no-console
    console.log(\`\\n=== API TestForge summary ===\\nTotal: \${results.length} | Passed: \${passed} | Failed: \${failed}\`);

    try {
      const reportDir = path.resolve(process.cwd(), "test-results");
      if (!fs.existsSync(reportDir)) fs.mkdirSync(reportDir, { recursive: true });
      fs.writeFileSync(
        path.join(reportDir, "api-testforge-report.json"),
        JSON.stringify({ generatedAt: new Date().toISOString(), totals: { total: results.length, passed, failed }, results }, null, 2),
      );
    } catch (err) {
      // eslint-disable-next-line no-console
      console.warn("Could not write report:", err);
    }
  });
});
`;
}

export function buildEnvExample(config: RequestConfig): string {
  const tokenEnv = config.auth.envVarName || "API_TOKEN";
  const needToken = config.auth.mode !== "none";
  return `BASE_URL=${config.baseUrl}\n${needToken ? `${tokenEnv}=your_token_here\n` : ""}`;
}
